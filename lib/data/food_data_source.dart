Food getFood(String name) => Food(
      name: name,
      price: Random().price(),
      image: 'https://source.unsplash.com/random?$name',
      calories: Random().nextInt(100) + 50,
      time: Random().nextInt(30) + 3,
      portions: Random().nextInt(5) + 3,
      sugar: Random().nextInt(5) + 3,
      salt: Random().nextInt(5) + 3,
      fat: Random().nextInt(5) + 3,
      saturates: Random().nextInt(5) + 3,
    );

Food get randomFood {
  final name = _foodNames[Random().nextInt(_foodNames.length)];
  return getFood(name);
}

extension on Random {
  double price() {
    double value = Random().nextDouble() * 21;
    return (value * 100).toInt() / 100;
  }
}
